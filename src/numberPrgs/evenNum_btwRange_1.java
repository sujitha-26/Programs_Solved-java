package numberPrgs;

public class evenNum_btwRange_1 {

	public static void main(String[] args) {
		int num=1;
		while(num<=10) 
		{
			if(num%2==0) 
			{
				System.out.println(num);
				
			}
			num++;
			
		}
		System.out.println("==========");
		for(int i=1;i<=10;i++) 
		{
			if(i%2==0) 
			{
				System.out.println(i);
			}
		}

	}

}
